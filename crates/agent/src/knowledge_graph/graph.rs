    /// Return the singleton System node id, creating it if necessary.
    ///
    /// The first caller may not know the real hostname (e.g. the decision
    /// recording path calls `ensure_system("")` before any event has flowed
    /// through). In that case the node is created with an empty label, and
    /// later calls that *do* have a real hostname will upgrade the label in
    /// place. Once a non-empty hostname is recorded it becomes immutable —
    /// the graph is single-host so there's no legitimate reason to rename.
    pub fn ensure_system(&mut self, hostname: &str) -> NodeId {
        if let Some(id) = self.system_node {
            // Late-upgrade: fill an empty hostname the first time a real one
            // shows up. This fixes the bug where the System node was stuck
            // with "" because the decision path happened to create it before
            // any ingested event did.
            if !hostname.is_empty() {
                if let Some(Node::System { hostname: h, .. }) = self.get_node_mut(id) {
                    if h.is_empty() {
                        *h = hostname.to_string();
                    }
                }
            }
            return id;
        }
        let id = self.add_node(Node::System {
            hostname: hostname.to_string(),
            sysctl_params: HashMap::new(),
        });
        self.system_node = Some(id);
        id
    }

    // ── Traversal primitives ────────────────────────────────────────────

    pub fn outgoing_edges(&self, node: NodeId) -> Vec<&Edge> {
        self.outgoing
            .get(&node)
            .map(|idxs| idxs.iter().filter_map(|&i| self.edges.get(i)).collect())
            .unwrap_or_default()
    }

    pub fn incoming_edges(&self, node: NodeId) -> Vec<&Edge> {
        self.incoming
            .get(&node)
            .map(|idxs| idxs.iter().filter_map(|&i| self.edges.get(i)).collect())
            .unwrap_or_default()
    }

    /// All edges (in + out) for a node, sorted by timestamp. Deduplicated by index.
    pub fn all_edges(&self, node: NodeId) -> Vec<&Edge> {
        let mut seen = HashSet::new();
        let out = self
            .outgoing
            .get(&node)
            .map(|v| v.as_slice())
            .unwrap_or(&[]);
        let inc = self
            .incoming
            .get(&node)
            .map(|v| v.as_slice())
            .unwrap_or(&[]);

        let mut edges: Vec<&Edge> = out
            .iter()
            .chain(inc.iter())
            .filter(|&&idx| seen.insert(idx))
            .filter_map(|&idx| self.edges.get(idx))
            .collect();
        edges.sort_by_key(|e| e.ts);
        edges
    }

    /// Find all nodes of a given type.
    pub fn nodes_of_type(&self, nt: NodeType) -> Vec<NodeId> {
        self.nodes
            .iter()
            .filter(|(_, n)| n.node_type() == nt)
            .map(|(&id, _)| id)
            .collect()
    }

    /// Find nodes that have edges newer than `since`. Much faster than scanning all nodes.
    pub fn active_nodes_since(&self, since: DateTime<Utc>) -> HashSet<NodeId> {
        let mut active = HashSet::new();
        // Scan edges from the end (most recent) backwards
        for edge in self.edges.iter().rev() {
            if edge.ts < since {
                break; // Edges are appended chronologically
            }
            active.insert(edge.from);
            active.insert(edge.to);
        }
        active
    }

    /// BFS neighborhood: subgraph within `depth` hops of `start`.
    pub fn neighborhood(&self, start: NodeId, depth: usize) -> SubGraph {
        // Caps prevent the BFS from exploding on hot nodes (a single attacker
        // IP can have thousands of incident edges). The frontend cytoscape viz
        // is unusable beyond a few hundred nodes anyway, and /api/graph/neighborhood
        // was the reason the dashboard hung on Investigation pivots.
        const MAX_TOTAL_EDGES: usize = 500;
        const MAX_EDGES_PER_NODE: usize = 50;

        let mut sub = SubGraph::default();
        let mut visited: HashSet<NodeId> = HashSet::new();
        let mut queue: VecDeque<(NodeId, usize)> = VecDeque::new();

        if let Some(node) = self.nodes.get(&start) {
            visited.insert(start);
            sub.nodes.insert(start, node.clone());
            queue.push_back((start, 0));
        }

        'bfs: while let Some((current, d)) = queue.pop_front() {
            if d >= depth {
                continue;
            }

            let mut per_node_edges = 0usize;

            // Outgoing (capped per-node)
            for edge in self.outgoing_edges(current) {
                if sub.edges.len() >= MAX_TOTAL_EDGES {
                    break 'bfs;
                }
                if per_node_edges >= MAX_EDGES_PER_NODE {
                    break;
                }
                sub.edges.push(edge.clone());
                per_node_edges += 1;
                if !visited.contains(&edge.to) {
                    visited.insert(edge.to);
                    if let Some(n) = self.nodes.get(&edge.to) {
                        sub.nodes.insert(edge.to, n.clone());
                    }
                    queue.push_back((edge.to, d + 1));
                }
            }

            // Incoming (capped per-node, shares budget with outgoing)
            for edge in self.incoming_edges(current) {
                if sub.edges.len() >= MAX_TOTAL_EDGES {
                    break 'bfs;
                }
                if per_node_edges >= MAX_EDGES_PER_NODE * 2 {
                    break;
                }
                sub.edges.push(edge.clone());
                per_node_edges += 1;
                if !visited.contains(&edge.from) {
                    visited.insert(edge.from);
                    if let Some(n) = self.nodes.get(&edge.from) {
                        sub.nodes.insert(edge.from, n.clone());
                    }
                    queue.push_back((edge.from, d + 1));
                }
            }
        }

        sub
    }

    /// BFS shortest path between two nodes. Returns edge chain or None.
    pub fn path_between(&self, from: NodeId, to: NodeId, max_depth: usize) -> Option<Vec<Edge>> {
        if from == to {
            return Some(Vec::new());
        }

        let mut visited: HashSet<NodeId> = HashSet::new();
        // (current_node, path_of_edge_indexes)
        let mut queue: VecDeque<(NodeId, Vec<usize>)> = VecDeque::new();

        visited.insert(from);
        queue.push_back((from, Vec::new()));

        while let Some((current, path)) = queue.pop_front() {
            if path.len() >= max_depth {
                continue;
            }

            // Outgoing edges
            if let Some(idxs) = self.outgoing.get(&current) {
                for &idx in idxs {
                    if let Some(edge) = self.edges.get(idx) {
                        if edge.to == to {
                            let mut result: Vec<Edge> = path
                                .iter()
                                .filter_map(|&i| self.edges.get(i).cloned())
                                .collect();
                            result.push(edge.clone());
                            return Some(result);
                        }
                        if !visited.contains(&edge.to) {
                            visited.insert(edge.to);
                            let mut new_path = path.clone();
                            new_path.push(idx);
                            queue.push_back((edge.to, new_path));
                        }
                    }
                }
            }

            // Incoming edges (bidirectional search)
            if let Some(idxs) = self.incoming.get(&current) {
                for &idx in idxs {
                    if let Some(edge) = self.edges.get(idx) {
                        if edge.from == to {
                            let mut result: Vec<Edge> = path
                                .iter()
                                .filter_map(|&i| self.edges.get(i).cloned())
                                .collect();
                            result.push(edge.clone());
                            return Some(result);
                        }
                        if !visited.contains(&edge.from) {
                            visited.insert(edge.from);
                            let mut new_path = path.clone();
                            new_path.push(idx);
                            queue.push_back((edge.from, new_path));
                        }
                    }
                }
            }
        }

        None
    }

    /// Process tree: all descendants via SpawnedBy edges (reversed).
    pub fn descendants(&self, pid: u32) -> Vec<NodeId> {
        let start = match self.find_by_pid(pid) {
            Some(id) => id,
            None => return Vec::new(),
        };

        let mut result = Vec::new();
        let mut queue = VecDeque::new();
        let mut visited = HashSet::new();

        queue.push_back(start);
        visited.insert(start);

        while let Some(current) = queue.pop_front() {
            // Children are nodes that have SpawnedBy edge pointing TO current
            if let Some(idxs) = self.incoming.get(&current) {
                for &idx in idxs {
                    if let Some(edge) = self.edges.get(idx) {
                        if edge.relation == Relation::SpawnedBy && !visited.contains(&edge.from) {
                            visited.insert(edge.from);
                            result.push(edge.from);
                            queue.push_back(edge.from);
                        }
                    }
                }
            }
        }

        result
    }

    /// Process tree: ancestors via SpawnedBy edges (forward).
    pub fn ancestors(&self, pid: u32) -> Vec<NodeId> {
        let start = match self.find_by_pid(pid) {
            Some(id) => id,
            None => return Vec::new(),
        };

        let mut result = Vec::new();
        let mut current = start;
        let mut visited = HashSet::new();
        visited.insert(current);

        loop {
            // Parent is the node this process has a SpawnedBy edge TO
            let parent = self.outgoing.get(&current).and_then(|idxs| {
                idxs.iter().find_map(|&idx| {
                    self.edges.get(idx).and_then(|e| {
                        if e.relation == Relation::SpawnedBy {
                            Some(e.to)
                        } else {
                            None
                        }
                    })
                })
            });

            match parent {
                Some(p) if !visited.contains(&p) => {
                    visited.insert(p);
                    result.push(p);
                    current = p;
                }
                _ => break,
            }
        }

        result
    }

    /// Timeline: all edges of a node sorted by timestamp, excluding snapshots.
    pub fn timeline(&self, node: NodeId) -> Vec<&Edge> {
        let mut edges = self.all_edges(node);
        edges.retain(|e| !e.is_snapshot());
        edges
    }

    /// Threat intel hits: processes connected to IPs with non-empty datasets.
    pub fn threat_intel_hits(&self) -> Vec<(NodeId, NodeId, String)> {
        let mut hits = Vec::new();
        for &ip_id in &self.threat_intel_nodes {
            let datasets = match self.nodes.get(&ip_id) {
                Some(Node::Ip { datasets, .. }) if !datasets.is_empty() => datasets.clone(),
                _ => continue,
            };

            // Find processes connected to this IP
            if let Some(idxs) = self.incoming.get(&ip_id) {
                for &idx in idxs {
                    if let Some(edge) = self.edges.get(idx) {
                        if edge.relation == Relation::ConnectedTo {
                            for ds in &datasets {
                                hits.push((edge.from, ip_id, ds.clone()));
                            }
                        }
                    }
                }
            }
        }
        hits
    }

