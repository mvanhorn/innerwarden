    /// Phase 014-C: Link incidents in a correlation chain via CorrelatedWith edges.
    /// Given a list of incident IDs that form a multi-stage attack chain,
    /// connects each pair with a CorrelatedWith edge for graph traversal.
    #[allow(dead_code)] // entity-overlap path wires chains today; kept for explicit chain-id usage
    pub fn link_correlated_incidents(&mut self, incident_ids: &[String], chain_id: &str) {
        let node_ids: Vec<NodeId> = incident_ids
            .iter()
            .filter_map(|iid| self.find_by_incident(iid))
            .collect();
        if node_ids.len() < 2 {
            return;
        }
        let now = chrono::Utc::now();
        // Create a complete graph of CorrelatedWith edges between all pairs
        for i in 0..node_ids.len() {
            for j in (i + 1)..node_ids.len() {
                let edge = Edge::new(node_ids[i], node_ids[j], Relation::CorrelatedWith, now)
                    .with_prop("chain_id", serde_json::Value::from(chain_id));
                self.add_edge(edge);
            }
        }
    }

    // ── Ensure helpers (find or create) ─────────────────────────────────

    pub fn ensure_process(
        &mut self,
        pid: u32,
        ppid: u32,
        comm: &str,
        uid: u32,
        ts: DateTime<Utc>,
    ) -> NodeId {
        if let Some(id) = self.find_by_pid(pid) {
            // Backfill missing fields from later events that carry richer context.
            // ensure_process is called from many event types (exec, exit, openat,
            // ptrace, etc) — earlier ones may lack comm/ppid/uid. Without backfill,
            // 70% of process nodes end up with empty comm in production.
            if let Some(Node::Process {
                comm: existing_comm,
                ppid: existing_ppid,
                uid: existing_uid,
                ..
            }) = self.nodes.get_mut(&id)
            {
                if existing_comm.is_empty() && !comm.is_empty() {
                    *existing_comm = comm.to_string();
                }
                if *existing_ppid == 0 && ppid > 0 {
                    *existing_ppid = ppid;
                }
                if *existing_uid == 0 && uid > 0 {
                    *existing_uid = uid;
                }
            }
            return id;
        }
        self.add_node(Node::Process {
            pid,
            ppid,
            comm: comm.to_string(),
            exe: None,
            uid,
            container_id: None,
            start_ts: ts,
            exit_ts: None,
        })
    }

    pub fn ensure_ip(&mut self, addr: &str, ts: DateTime<Utc>) -> NodeId {
        if let Some(id) = self.find_by_ip(addr) {
            // Update last_seen
            if let Some(Node::Ip { last_seen, .. }) = self.nodes.get_mut(&id) {
                *last_seen = ts;
            }
            return id;
        }
        self.add_node(Node::Ip {
            addr: addr.to_string(),
            is_internal: is_internal_ip(addr),
            datasets: Vec::new(),
            risk_score: 0,
            is_tor: false,
            first_seen: ts,
            last_seen: ts,
            attempted_usernames: Vec::new(),
        })
    }

    /// Spec 015: record an attacker-supplied username from a failed SSH auth.
    /// Appends to the Ip node's `attempted_usernames` list, deduplicating and
    /// capping at 50 most-recent entries (LIFO). Silently no-ops if the node
    /// is not an Ip.
    pub fn record_attempted_username(&mut self, ip_id: NodeId, attacker_login: &str) {
        const MAX_ATTEMPTED: usize = 50;
        if attacker_login.is_empty() {
            return;
        }
        if let Some(Node::Ip {
            attempted_usernames,
            ..
        }) = self.nodes.get_mut(&ip_id)
        {
            // lgtm[rust/cleartext-logging] — these are attacker-supplied brute-force
            // login names (e.g. "root", "admin"), NOT real credentials.
            // Stored for threat DNA fingerprinting.
            if let Some(pos) = attempted_usernames.iter().position(|u| u == attacker_login) {
                attempted_usernames.remove(pos);
            }
            attempted_usernames.push(attacker_login.to_string());
            // Cap (LIFO): drop the oldest entries when exceeding the max.
            if attempted_usernames.len() > MAX_ATTEMPTED {
                let drop = attempted_usernames.len() - MAX_ATTEMPTED;
                attempted_usernames.drain(0..drop);
            }
        }
    }

    /// Spec 015: retrieve attacker-supplied usernames from a given Ip node,
    /// for attacker fingerprinting / threat-dna features.
    #[allow(dead_code)] // API surface for future attacker-intel consumers
    pub fn attempted_usernames_for_ip(&self, ip_id: NodeId) -> Vec<String> {
        match self.nodes.get(&ip_id) {
            Some(Node::Ip {
                attempted_usernames,
                ..
            }) => attempted_usernames.clone(),
            _ => Vec::new(),
        }
    }

