    /// Count outgoing edges of a specific relation type within a time window.
    pub fn count_edges_in_window(
        &self,
        node: NodeId,
        relation: Relation,
        now: chrono::DateTime<chrono::Utc>,
        window: chrono::Duration,
    ) -> usize {
        self.outgoing_edges(node)
            .iter()
            .filter(|e| e.relation == relation && now - e.ts < window)
            .count()
    }

    /// Count distinct destination nodes connected via a relation in a time window.
    pub fn count_distinct_targets_in_window(
        &self,
        node: NodeId,
        relation: Relation,
        now: chrono::DateTime<chrono::Utc>,
        window: chrono::Duration,
    ) -> usize {
        let targets: std::collections::HashSet<NodeId> = self
            .outgoing_edges(node)
            .iter()
            .filter(|e| e.relation == relation && now - e.ts < window)
            .map(|e| e.to)
            .collect();
        targets.len()
    }

