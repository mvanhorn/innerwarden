/// In-memory knowledge graph with O(1) indexes for all critical queries.
pub struct KnowledgeGraph {
    // ── Storage ──
    pub(crate) nodes: HashMap<NodeId, Node>,
    pub(crate) edges: Vec<Edge>,

    // ── Dedup indexes (entity → NodeId) ──
    pub(crate) pid_index: HashMap<u32, NodeId>,
    pub(crate) ip_index: HashMap<String, NodeId>,
    pub(crate) file_index: HashMap<String, NodeId>,
    pub(crate) user_index: HashMap<String, NodeId>,
    pub(crate) domain_index: HashMap<String, NodeId>,
    pub(crate) port_index: HashMap<(u16, String), NodeId>,
    pub(crate) container_index: HashMap<String, NodeId>,
    pub(crate) device_index: HashMap<String, NodeId>, // "vendor:product:serial"
    pub(crate) incident_index: HashMap<String, NodeId>,
    pub(crate) campaign_index: HashMap<String, NodeId>,
    pub(crate) system_node: Option<NodeId>,

    // ── Adjacency lists (NodeId → edge indexes) ──
    pub(crate) outgoing: HashMap<NodeId, Vec<usize>>,
    pub(crate) incoming: HashMap<NodeId, Vec<usize>>,

    /// Per-node "most recent edge timestamp", used by `enforce_memory_limit`
    /// to evict by LRU without paying O(E_avg log E_avg) per node. Updated on
    /// every `add_edge`, cleared on `remove_node`. Rebuilt from `edges` on
    /// `reconstruct_from_snapshot` (same precedent as `outgoing`/`incoming`),
    /// so it does not enter the persistence wire format.
    pub(crate) last_edge_ts: HashMap<NodeId, DateTime<Utc>>,

    // ── Fast-access sets ──
    pub(crate) threat_intel_nodes: HashSet<NodeId>,

    // ── Counters ──
    pub(crate) next_id: NodeId,
    pub(crate) memory_estimate: usize,
    pub(crate) tombstoned_edges: usize,
    pub max_memory: usize,
    pub created_at: DateTime<Utc>,

    // ── Event telemetry (for sensors tab) ──
    pub(crate) source_counts: HashMap<String, usize>,
    pub(crate) kind_counts: HashMap<String, usize>,
    pub(crate) event_timeline: std::collections::BTreeMap<String, HashMap<String, usize>>,
    pub(crate) total_events_ingested: usize,

    // ── Transient: current event metadata (set during ingest, used by add_edge) ──
    pub(crate) _current_event_source: Option<String>,
    pub(crate) _current_event_kind: Option<String>,
    pub(crate) _current_event_summary: Option<String>,
    pub(crate) _current_event_severity: Option<String>,

    // ── Real-time trigger state ──
    /// Incidents generated by edge-insert triggers (collected during ingest, drained by caller).
    pub(crate) trigger_incidents: Vec<innerwarden_core::incident::Incident>,
    /// Host label for trigger incident generation.
    pub(crate) trigger_host: String,
}

