    // ── Node CRUD ───────────────────────────────────────────────────────

    /// Add a node, returning its ID. If a node with the same dedup key exists,
    /// returns the existing ID without creating a duplicate.
    pub fn add_node(&mut self, node: Node) -> NodeId {
        // Check dedup
        if let Some(existing) = self.find_existing(&node) {
            return existing;
        }

        let id = self.next_id;
        self.next_id += 1;

        self.index_node(id, &node);
        self.memory_estimate += Self::estimate_node_size(&node);
        self.nodes.insert(id, node);

        id
    }

    /// Add or update: if the dedup key exists, update the node in-place and return its ID.
    /// Otherwise create a new node.
    pub fn upsert_node(&mut self, node: Node) -> NodeId {
        if let Some(existing_id) = self.find_existing(&node) {
            // Update the existing node with new data
            if let Some(existing) = self.nodes.get_mut(&existing_id) {
                Self::merge_node(existing, &node);
            }
            // Update threat intel index if needed
            if let Node::Ip { ref datasets, .. } = node {
                if !datasets.is_empty() {
                    self.threat_intel_nodes.insert(existing_id);
                }
            }
            existing_id
        } else {
            self.add_node(node)
        }
    }

    pub fn get_node(&self, id: NodeId) -> Option<&Node> {
        self.nodes.get(&id)
    }

    pub fn get_node_mut(&mut self, id: NodeId) -> Option<&mut Node> {
        self.nodes.get_mut(&id)
    }

    pub fn remove_node(&mut self, id: NodeId) {
        if let Some(node) = self.nodes.remove(&id) {
            self.memory_estimate = self
                .memory_estimate
                .saturating_sub(Self::estimate_node_size(&node));
            self.deindex_node(id, &node);
            self.threat_intel_nodes.remove(&id);
            self.last_edge_ts.remove(&id);

            // Collect edge indexes to tombstone
            let mut edge_idxs: Vec<usize> = self.outgoing.remove(&id).unwrap_or_default();
            edge_idxs.extend(self.incoming.remove(&id).unwrap_or_default());
            edge_idxs.sort_unstable();
            edge_idxs.dedup();

            for idx in &edge_idxs {
                if *idx < self.edges.len() {
                    let edge = &self.edges[*idx];
                    let other = if edge.from == id { edge.to } else { edge.from };
                    if let Some(adj) = self.outgoing.get_mut(&other) {
                        adj.retain(|&i| i != *idx);
                    }
                    if let Some(adj) = self.incoming.get_mut(&other) {
                        adj.retain(|&i| i != *idx);
                    }
                }
                self.tombstoned_edges += 1;
            }
        }
    }

    /// Compact the edge vec by removing tombstoned entries and rebuilding adjacency.
    /// Called periodically when tombstone ratio exceeds 20%.
    ///
    /// 2026-05-03 (Wave 5b PR-4): the 20% ratio gate exists to amortise
    /// the O(E) rebuild cost across many remove_node calls. It is the
    /// right behaviour during steady-state ingest. It is NOT the right
    /// behaviour right before a snapshot serialise — tombstoned edges
    /// would leak into the persisted blob and reload would tag them
    /// as "dangling" (operator-visible WARN every time the snapshot
    /// is round-tripped). The slow_loop path now calls
    /// `compact_edges_force` instead; this gated method is retained
    /// as the canonical inline-mutation entry point for any future
    /// site that needs amortised compaction without forcing a
    /// full rebuild on every call.
    #[allow(dead_code)]
    pub fn compact_edges(&mut self) {
        if self.edges.is_empty() || self.tombstoned_edges == 0 {
            return;
        }
        let ratio = self.tombstoned_edges as f32 / self.edges.len() as f32;
        if ratio < 0.2 {
            return;
        }

        // Collect live edges (those still referenced by adjacency lists)
        let mut live: HashSet<usize> = HashSet::new();
        for idxs in self.outgoing.values() {
            for &i in idxs {
                live.insert(i);
            }
        }

        let mut new_edges = Vec::with_capacity(live.len());
        let mut old_to_new: HashMap<usize, usize> = HashMap::new();

        for (old_idx, edge) in self.edges.iter().enumerate() {
            if live.contains(&old_idx) {
                let new_idx = new_edges.len();
                old_to_new.insert(old_idx, new_idx);
                new_edges.push(edge.clone());
            } else {
                self.memory_estimate = self
                    .memory_estimate
                    .saturating_sub(Self::estimate_edge_size(edge));
            }
        }

        // Remap adjacency lists
        for idxs in self.outgoing.values_mut() {
            *idxs = idxs
                .iter()
                .filter_map(|i| old_to_new.get(i).copied())
                .collect();
        }
        for idxs in self.incoming.values_mut() {
            *idxs = idxs
                .iter()
                .filter_map(|i| old_to_new.get(i).copied())
                .collect();
        }

        let removed = self.edges.len() - new_edges.len();
        self.edges = new_edges;
        self.tombstoned_edges = 0;

        if removed > 0 {
            tracing::debug!(
                removed,
                live = self.edges.len(),
                "knowledge graph: compacted edges"
            );
        }
    }

    /// 2026-05-03 (Wave 5b PR-4): unconditional compaction for the
    /// pre-serialise path. Same body as `compact_edges` minus the
    /// 20%-ratio gate. Use this from slow_loop right before
    /// `serialize_snapshot_bytes` so tombstoned edges accumulated by
    /// `enforce_memory_limit` and `cleanup_expired` do not leak into
    /// the persisted blob.
    ///
    /// Why a separate method instead of dropping the gate from
    /// `compact_edges`: the gated path is the right behaviour during
    /// inline mutations (avoid O(E) work on every remove). The
    /// unconditional path is the right behaviour when the next
    /// operation is a serialise — we are about to walk every edge
    /// anyway, so paying compaction now amortises into the same scan.
    pub fn compact_edges_force(&mut self) {
        if self.edges.is_empty() || self.tombstoned_edges == 0 {
            return;
        }

        // Same body as `compact_edges` from here on. Kept as a
        // dedicated method (not a refactor that calls into a shared
        // helper) because the gate is the only difference and
        // splitting further would obscure the call graph the heap-
        // budget anchor walks.
        let mut live: HashSet<usize> = HashSet::new();
        for idxs in self.outgoing.values() {
            for &i in idxs {
                live.insert(i);
            }
        }

        let mut new_edges = Vec::with_capacity(live.len());
        let mut old_to_new: HashMap<usize, usize> = HashMap::new();

        for (old_idx, edge) in self.edges.iter().enumerate() {
            if live.contains(&old_idx) {
                let new_idx = new_edges.len();
                old_to_new.insert(old_idx, new_idx);
                new_edges.push(edge.clone());
            } else {
                self.memory_estimate = self
                    .memory_estimate
                    .saturating_sub(Self::estimate_edge_size(edge));
            }
        }

        for idxs in self.outgoing.values_mut() {
            *idxs = idxs
                .iter()
                .filter_map(|i| old_to_new.get(i).copied())
                .collect();
        }
        for idxs in self.incoming.values_mut() {
            *idxs = idxs
                .iter()
                .filter_map(|i| old_to_new.get(i).copied())
                .collect();
        }

        let removed = self.edges.len() - new_edges.len();
        self.edges = new_edges;
        self.tombstoned_edges = 0;

        if removed > 0 {
            tracing::debug!(
                removed,
                live = self.edges.len(),
                "knowledge graph: force-compacted edges before snapshot"
            );
        }
    }

    // ── Edge CRUD ───────────────────────────────────────────────────────

    /// Add an edge. Edges are never deduplicated — each represents a discrete event.
    /// If event metadata is set (during ingest), automatically adds source/kind/summary.
    pub fn add_edge(&mut self, mut edge: Edge) -> usize {
        // Enrich edge with current event metadata if available. All
        // four hot keys go through the interner (per-key) so the
        // 145k+ edges in prod share four `Arc<str>` allocations
        // instead of paying a fresh `String` per insert.
        use crate::knowledge_graph::intern::intern;
        if let Some(ref src) = self._current_event_source {
            if !edge.properties.contains_key("event_source") {
                edge.properties.insert(
                    intern("event_source"),
                    serde_json::Value::from(src.as_str()),
                );
            }
        }
        if let Some(ref kind) = self._current_event_kind {
            if !edge.properties.contains_key("event_kind") {
                edge.properties
                    .insert(intern("event_kind"), serde_json::Value::from(kind.as_str()));
            }
        }
        if let Some(ref summary) = self._current_event_summary {
            if !summary.is_empty() && !edge.properties.contains_key("summary") {
                // Truncate summary to save memory
                let trunc = if summary.len() > 200 {
                    &summary[..200]
                } else {
                    summary.as_str()
                };
                edge.properties
                    .insert(intern("summary"), serde_json::Value::from(trunc));
            }
        }
        if let Some(ref sev) = self._current_event_severity {
            if !edge.properties.contains_key("severity") {
                edge.properties
                    .insert(intern("severity"), serde_json::Value::from(sev.as_str()));
            }
        }

        let idx = self.edges.len();
        self.outgoing.entry(edge.from).or_default().push(idx);
        self.incoming.entry(edge.to).or_default().push(idx);
        self.memory_estimate += Self::estimate_edge_size(&edge);

        // Maintain `last_edge_ts` index for both endpoints. This replaces the
        // O(E_avg log E_avg) per-node `all_edges()` scan that
        // `enforce_memory_limit` used to do under memory pressure (the worst
        // possible time to allocate). See RECURRING_BUGS.md
        // "enforce_memory_limit allocates O(N × E) under memory pressure".
        let edge_from = edge.from;
        let edge_to = edge.to;
        let edge_ts = edge.ts;
        self.last_edge_ts
            .entry(edge_from)
            .and_modify(|t| {
                if edge_ts > *t {
                    *t = edge_ts;
                }
            })
            .or_insert(edge_ts);
        if edge_to != edge_from {
            self.last_edge_ts
                .entry(edge_to)
                .and_modify(|t| {
                    if edge_ts > *t {
                        *t = edge_ts;
                    }
                })
                .or_insert(edge_ts);
        }

        self.edges.push(edge);

        // Real-time critical triggers (O(1) checks per edge)
        // Clone the minimal data needed to avoid borrow conflict
        let trigger_rel = self.edges[idx].relation;
        let trigger_from = self.edges[idx].from;
        let trigger_to = self.edges[idx].to;
        let trigger_ts = self.edges[idx].ts;
        let trigger_props = self.edges[idx].properties.clone();
        if !self.edges[idx].is_snapshot() {
            super::triggers::check_critical_triggers(
                self,
                trigger_rel,
                trigger_from,
                trigger_to,
                trigger_ts,
                &trigger_props,
            );
        }

        idx
    }

